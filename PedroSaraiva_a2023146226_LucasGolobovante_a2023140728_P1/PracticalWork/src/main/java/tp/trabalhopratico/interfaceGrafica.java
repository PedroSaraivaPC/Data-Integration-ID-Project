    private void ModoEscuroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModoEscuroActionPerformed
        // TODO add your handling code here:
        boolean modoEscuro = ModoEscuro.isSelected();

        Color corFundo = modoEscuro ? new Color(45, 45, 45) : Color.WHITE;
        Color corTexto = modoEscuro ? Color.LIGHT_GRAY : Color.BLACK;

        // Muda o fundo da janela principal
        this.getContentPane().setBackground(corFundo);

        // Aplica às áreas de texto e scroll, labels, etc
        jTextArea1.setBackground(corFundo); //  cor do fundo
        jTextArea1.setForeground(corTexto); //  cor do texto
        jTextArea1.setCaretColor(corTexto); //  cor do cursor de texto
        jScrollPane1.getViewport().setBackground(corFundo); //  cor dos scrolls do panel da area de texto
        jLabel5.setForeground(corTexto);

        // Aplica ao menu principal
        MenuPrincipal.setBackground(corFundo);
        MenuPrincipal.setForeground(corTexto);
        for (int i = 0; i < MenuPrincipal.getMenuCount(); i++) {
            JMenu menu = MenuPrincipal.getMenu(i);
            menu.setBackground(corFundo);
            menu.setForeground(corTexto);
            for (int j = 0; j < menu.getItemCount(); j++) {
                JMenuItem item = menu.getItem(j);
                if (item != null) {
                    item.setBackground(corFundo);
                    item.setForeground(corTexto);
                }
            }
        }
    }//GEN-LAST:event_ModoEscuroActionPerformed

