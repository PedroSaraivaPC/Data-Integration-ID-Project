    private void jButton28ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton28ActionPerformed
        // 4a transformacao
        try {
            String nomePais = JOptionPane.showInputDialog(this, "Insere o nome do país:", "Filtro por país", JOptionPane.QUESTION_MESSAGE);
            if(nomePais == null){
                return;
            }
            if(nomePais.trim().length() == 0){
                JOptionPane.showMessageDialog(this, "Insira o nome de um país antes de avançar.", "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            }
            String xp = "//pais[contains(@nome,'" + nomePais + "')]";
            XdmValue res = XPathFunctions.executaXpath(xp, "paises.xml");
            if (res == null || res.size() == 0) {
                JOptionPane.showMessageDialog(this, "O país não existe no ficheiro.", "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            }
            TransformerFactory tFactory = TransformerFactory.newInstance();
            Transformer transformer = null;
            try {
                transformer = tFactory.newTransformer(new StreamSource("xslt/transf4.xsl"));
            } catch (TransformerConfigurationException ex) {
                Logger.getLogger(interfaceGrafica.class.getName()).log(Level.SEVERE, null, ex);
            }

            // Passar o nome do país como parâmetro para o XSLT
            transformer.setParameter("paisNome", nomePais.trim());

            try {
                transformer.transform(
                        new StreamSource(new FileInputStream("paises.xml")),
                        new StreamResult(new FileOutputStream("xslt/outputs/paisesFronteira.html"))
                );
            } catch (TransformerException ex) {
                Logger.getLogger(interfaceGrafica.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(this,"Query feita com sucesso... ficheiro HTML criado! ","XSLT",JOptionPane.INFORMATION_MESSAGE);

            File htmlFile = new File("xslt/outputs/paisesFronteira.html");
            Desktop.getDesktop().browse(htmlFile.toURI());
        } catch (IOException ex) {
            Logger.getLogger(interfaceGrafica.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SaxonApiException ex) {
            Logger.getLogger(interfaceGrafica.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton28ActionPerformed

