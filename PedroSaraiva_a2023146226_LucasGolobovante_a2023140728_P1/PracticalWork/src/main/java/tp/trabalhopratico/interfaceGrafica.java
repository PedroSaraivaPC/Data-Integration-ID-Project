    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // Editar um pais
        String nomePais = jTextField3.getText().trim();
        String atributoSelecionado = jComboBox1.getSelectedItem().toString();
        String novoValor = jTextField4.getText().trim();
        if(nomePais == null || nomePais.isBlank()){ // chekar se tem algo escrito no campo de texto do pais
            JOptionPane.showMessageDialog(this, "Escreva um país para avançar.", "Editar País", JOptionPane.WARNING_MESSAGE);
            return;
        } else if(novoValor == null || novoValor.isBlank()){ // chekar se tem algo escrito no campo do novo valor a substituir
            JOptionPane.showMessageDialog(this, "Escreva um novo valor para avançar.", "Editar País", JOptionPane.WARNING_MESSAGE);
            return;
        } else{
            if (atributoSelecionado.equals("capital") || atributoSelecionado.equals("continente") || atributoSelecionado.equals("img_bandeira") || atributoSelecionado.equals("presidente") || atributoSelecionado.equals("moeda") || atributoSelecionado.equals("hino")) {
            // Aceita qualquer coisa do tipo String
            } else if (atributoSelecionado.equals("area")) {
                // Cheka se o valor inserido é Double
                try {
                    Double.parseDouble(novoValor);
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "O valor para '" + atributoSelecionado + "' deve ser um número decimal (double).", "Valor inválido", JOptionPane.WARNING_MESSAGE);
                    return;
                }
            } else if (atributoSelecionado.equals("habitantes") || atributoSelecionado.equals("habitantes_porKM") || atributoSelecionado.equals("casos_covid") || atributoSelecionado.equals("mortes_covid")) {
                // Cheka se o valor inserido é Int
                try {
                    Integer.parseInt(novoValor);
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "O valor para '" + atributoSelecionado + "' deve ser um número inteiro.", "Valor inválido", JOptionPane.WARNING_MESSAGE);
                    return;
                }
            }
            
            // Tudo validado — tenta editar o XML
            Document doc = XMLJDomFunctions.lerDocumentoXML("paises.xml");
            doc = ModeloXML.alteraAtributoNaoLista(nomePais, atributoSelecionado, novoValor, doc);

            if (doc != null) {
                XMLJDomFunctions.escreverDocumentoParaFicheiro(doc, "paises.xml");

                JOptionPane.showMessageDialog(this, "Atributo '" + atributoSelecionado + "' do país \"" + nomePais + "\" alterado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                jTextField3.setText("");
                jTextField4.setText("");
                EditarPais.dispose();
                jComboBox1.setSelectedItem("capital");

                // Atualiza output
                String texto = XMLJDomFunctions.escreverDocumentoString(doc);
                jTextArea1.setText(texto);
            } else {
                JOptionPane.showMessageDialog(this, "País \"" + nomePais + "\" não encontrado.", "Erro", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton6ActionPerformed

